//OPERATORS

#include<stdio.h>
int main(){
	int a=10;
	printf("a=%d\n",a);
	printf("++a=%d\n",++a);
	printf("a=%d\n",a);
	printf("a++=%d\n",a++);
	printf("a=%d\n",a);

	int b=5;
	printf("b=%d\n",b);
	printf("--b=%d\n",--b);
	printf("b=%d\n",b);
	printf("b--=%d\n",b--);
	printf("b=%d\n",b);
}
	
